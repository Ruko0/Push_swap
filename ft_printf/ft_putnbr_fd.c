#include "ft_printf.h"

int	ft_putnbr_fd(int n, int fd)
{
	int			bomba9la;
	int long	nb;

	bomba9la = c_int(n);
	nb = n;
	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		nb = -nb;
	}
	if (nb > 9)
	{
		ft_putnbr_fd(nb / 10, fd);
	}
	ft_putchar_fd(nb % 10 + '0', fd);
	return (bomba9la);
}
