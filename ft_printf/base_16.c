#include "ft_printf.h"

int	base_16(unsigned long n, char *base)
{
	int	i;

	i = c_hex(n);
	if (n > 15)
	{
		base_16(n / 16, base);
	}
	ft_putchar_fd(base[n % 16], 1);
	return (i);
}
